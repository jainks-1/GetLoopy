public class CountThreesTo18 {
    public static void main(String[] args) {

        System.out.print("\nCount to 18 in multiples of 3...\n");

        // count up to 18 from 0 in multiples of 3
        for (int count = 0; count <= 18; count+= 3) {

            System.out.print(count + "   ");
        }

        // finish and exit
        System.out.print("\nComplete!");
        System.exit(0);
    }
}


