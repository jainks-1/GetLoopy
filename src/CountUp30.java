public class CountUp30 {
    public static void main(String[] args) {

        System.out.print("\nCount from 0 to 30...\n");

        // count to 30 using for loop
        for (int count = 0; count <= 30; count++) {

            System.out.print(count + "   ");
        }

        // finish and exit
        System.out.print("\nComplete!");
        System.exit(0);
    }
}